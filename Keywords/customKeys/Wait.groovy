class Wait {
	@Keyword
	def hardWait(int timeinSeconds) {
		Thread.sleep(timeinSeconds*1000)
	}

	/**
	 * wait for element not to be stale
	 */
	@Keyword
	def waitUntilElementIsNotStale(TestObject to,int timeInSeconds) {
		for(int i=0;i<timeInSeconds;i++) {
			try {
				WebElement element = WebUiBuiltInKeywords.findWebElement(to);
				element.isDisplayed();
				KeywordUtil.markPassed("Element is not stale")
				Thread.sleep(1000);
			} catch (WebElementNotFoundException e) {
				if(i>timeInSeconds) {
					KeywordUtil.markFailed("Element is stale even after waiting")
					break;
				}
				i++;
			}
		}
	}
	
}